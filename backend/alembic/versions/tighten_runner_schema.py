_RENAME_SEQUENCE = """
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM pg_class c
        JOIN pg_namespace n ON n.oid = c.relnamespace
        WHERE c.relkind = 'S'
          AND n.nspname = 'runner'
          AND c.relname = 'runner_connections_id_seq'
    ) AND NOT EXISTS (
        SELECT 1 FROM pg_class c
        JOIN pg_namespace n ON n.oid = c.relnamespace
        WHERE c.relkind = 'S'
          AND n.nspname = 'runner'
          AND c.relname = 'runner_sessions_id_seq'
    ) THEN
        ALTER SEQUENCE runner.runner_connections_id_seq
            RENAME TO runner_sessions_id_seq;
        RAISE NOTICE 'Renamed sequence runner_connections_id_seq -> runner_sessions_id_seq';
    END IF;
END $$;
"""


