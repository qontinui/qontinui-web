"""tighten runner schema after unify_runner_concepts

Revision ID: tighten_runner_schema
Revises: unify_runner_concepts
Create Date: 2026-04-28

Follow-up to :mod:`unify_runner_concepts`. Removes the now-cosmetic
``server_mode`` and ``dispatch_secret`` columns from ``runner.runners``
(no readers after Phase 2/5 unified the dispatch path onto WebSocket)
and renames the sequence to match the renamed table.

Changes
-------

1. Rename ``runner.runner_connections_id_seq`` →
   ``runner.runner_sessions_id_seq``. Postgres doesn't auto-rename
   sequences when you ``ALTER TABLE … RENAME``; this just brings the
   sequence name in line with the table.

2. Drop ``runner.runners.server_mode``. Phase 2's dispatcher dropped
   the ``server_mode = true`` filter; nothing reads the column now.

3. Drop ``runner.runners.dispatch_secret``. Phase 5A deleted the
   HTTP-with-bearer dispatch path; the WS dispatch channel doesn't
   need a per-runner secret because the WS itself is authenticated.

The downgrade re-adds both columns and the sequence-rename, but
``dispatch_secret`` rows must be regenerated by re-registering each
runner — the original values aren't recoverable.

Idempotency
-----------
Each step is wrapped in ``information_schema`` / ``pg_class``
existence checks so re-applying after a partial fail is a no-op.
"""

