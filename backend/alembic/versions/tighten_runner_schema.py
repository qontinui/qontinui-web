def _drop_column_if_exists(column: str) -> str:
    return f"""
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'runner'
          AND table_name = 'runners'
          AND column_name = '{column}'
    ) THEN
        ALTER TABLE runner.runners DROP COLUMN {column};
        RAISE NOTICE 'Dropped runner.runners.{column}';
    END IF;
END $$;
"""


