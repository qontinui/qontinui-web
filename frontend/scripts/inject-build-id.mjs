#!/usr/bin/env node
/**
 * Build-ID injection for qontinui-web.
 *
 * Two modes, controlled by argv[2]:
 *   "pre"   — runs before `next build`. Computes a build-id of the form
 *             `<git-sha-short>-<unix-ms>` (or just `<unix-ms>` if git is
 *             unavailable) and writes it into `src/generated/build-id.ts`
 *             which exports `BUILD_ID` as a constant. Also emits the value
 *             into `.env.production.local` so `process.env.NEXT_PUBLIC_BUILD_ID`
 *             is populated for the build.
 *   "post"  — runs after `next build`. Reads the template from
 *             `public/sw.js`, substitutes `__BUILD_ID__` with the persisted
 *             build-id from `src/generated/build-id.ts`, and writes the
 *             substituted SW into the standalone deployment tree under
 *             `.next/standalone/<workspace>/public/sw.js` (the location
 *             Next.js standalone expects deployments to serve `public/`
 *             from — Next does NOT copy it itself). Also drops a copy at
 *             `.next/standalone/public/sw.js` for flat-deploy scripts.
 *
 * The src `public/sw.js` stays as the template containing `__BUILD_ID__`.
 * Only build outputs are mutated.
 */

import { execSync } from 'node:child_process';
import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  statSync,
  writeFileSync,
} from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const FRONTEND_ROOT = resolve(__dirname, '..');

const BUILD_ID_FILE = join(FRONTEND_ROOT, 'src/generated/build-id.ts');
const ENV_FILE = join(FRONTEND_ROOT, '.env.production.local');
const SW_TEMPLATE = join(FRONTEND_ROOT, 'public/sw.js');
const STANDALONE_ROOT = join(FRONTEND_ROOT, '.next/standalone');

function gitShaShort() {
  try {
    return execSync('git rev-parse --short HEAD', {
      cwd: FRONTEND_ROOT,
      stdio: ['ignore', 'pipe', 'ignore'],
    })
      .toString()
      .trim();
  } catch {
    return null;
  }
}

function computeBuildId() {
  const sha = gitShaShort();
  const ms = Date.now();
  return sha ? `${sha}-${ms}` : `${ms}`;
}

function readPersistedBuildId() {
  if (!existsSync(BUILD_ID_FILE)) return null;
  const m = readFileSync(BUILD_ID_FILE, 'utf8').match(
    /BUILD_ID\s*=\s*['"]([^'"]+)['"]/
  );
  return m ? m[1] : null;
}

function preBuild() {
  const id = computeBuildId();
  mkdirSync(dirname(BUILD_ID_FILE), { recursive: true });
  writeFileSync(
    BUILD_ID_FILE,
    `// AUTO-GENERATED by scripts/inject-build-id.mjs — do not edit by hand.\n` +
      `// Regenerated on every \`npm run build\`.\n` +
      `export const BUILD_ID = '${id}';\n`,
    'utf8'
  );
  // Also expose to the Next.js build via env var so SSR + edge see the same id.
  writeFileSync(ENV_FILE, `NEXT_PUBLIC_BUILD_ID=${id}\n`, 'utf8');
  // eslint-disable-next-line no-console
  console.log(`[inject-build-id] pre: BUILD_ID=${id}`);
  return id;
}

/**
 * Find the directory inside `.next/standalone/` that mirrors the workspace
 * layout, e.g. `.next/standalone/qontinui-web/frontend/`. Next.js standalone
 * preserves the project's path relative to the monorepo root, so the exact
 * subpath isn't predictable across machines/CI checkout layouts. Search
 * for a child that contains a `.next/server` dir — that's the one Next
 * runs from.
 */
function findStandaloneAppDir() {
  if (!existsSync(STANDALONE_ROOT)) return null;
  // Direct hit — non-monorepo layouts.
  if (existsSync(join(STANDALONE_ROOT, '.next/server'))) {
    return STANDALONE_ROOT;
  }
  // Walk one or two levels deep to find a child with `.next/server`.
  const queue = [STANDALONE_ROOT];
  let depth = 0;
  while (queue.length > 0 && depth < 4) {
    const next = [];
    for (const dir of queue) {
      let entries;
      try {
        entries = readdirSync(dir);
      } catch {
        continue;
      }
      for (const entry of entries) {
        const child = join(dir, entry);
        let s;
        try {
          s = statSync(child);
        } catch {
          continue;
        }
        if (!s.isDirectory()) continue;
        if (existsSync(join(child, '.next/server'))) {
          return child;
        }
        next.push(child);
      }
    }
    queue.length = 0;
    queue.push(...next);
    depth += 1;
  }
  return null;
}

function postBuild() {
  // Use the persisted value the pre-step wrote so pre/post agree even if a
  // millisecond elapsed between them.
  const id = readPersistedBuildId() ?? computeBuildId();

  // Read the source template — it must still contain `__BUILD_ID__`.
  if (!existsSync(SW_TEMPLATE)) {
    // eslint-disable-next-line no-console
    console.error(`[inject-build-id] post: missing template at ${SW_TEMPLATE}`);
    process.exit(1);
  }
  const template = readFileSync(SW_TEMPLATE, 'utf8');
  if (!template.includes('__BUILD_ID__')) {
    // eslint-disable-next-line no-console
    console.warn(
      `[inject-build-id] post: template ${SW_TEMPLATE} contains no __BUILD_ID__ token — was it already substituted in-place? Skipping.`
    );
    return;
  }
  const substituted = template.replaceAll('__BUILD_ID__', id);

  // Targets: every plausible build output the deploy might serve from.
  // `next build` with `output: 'standalone'` does NOT copy `public/` into
  // the standalone tree (documented behavior — operator must copy public
  // themselves for deployment). We do that copy here AND substitute, so
  // the deployed SW already has the build-id baked in.
  const targets = [];
  const standaloneApp = findStandaloneAppDir();
  if (standaloneApp) {
    targets.push(join(standaloneApp, 'public/sw.js'));
  }
  // Also drop a copy at .next/standalone/public/sw.js for any deploy script
  // that flattens to that location — harmless if unused.
  targets.push(join(STANDALONE_ROOT, 'public/sw.js'));

  let touched = 0;
  for (const swPath of targets) {
    try {
      mkdirSync(dirname(swPath), { recursive: true });
      writeFileSync(swPath, substituted, 'utf8');
      touched += 1;
      // eslint-disable-next-line no-console
      console.log(`[inject-build-id] post: wrote ${swPath} (BUILD_ID=${id})`);
    } catch (err) {
      // eslint-disable-next-line no-console
      console.warn(`[inject-build-id] post: failed to write ${swPath}: ${err}`);
    }
  }

  if (touched === 0) {
    // Not necessarily an error — if `output: 'standalone'` is off or the
    // user only ran `next dev`, there's nothing to substitute.
    // eslint-disable-next-line no-console
    console.log(
      `[inject-build-id] post: no SW output written (no standalone build detected); template at ${SW_TEMPLATE} left as-is.`
    );
  }
}

const mode = process.argv[2] ?? 'pre';
if (mode === 'pre') {
  preBuild();
} else if (mode === 'post') {
  postBuild();
} else {
  // eslint-disable-next-line no-console
  console.error(`[inject-build-id] unknown mode '${mode}', expected 'pre' or 'post'`);
  process.exit(2);
}
